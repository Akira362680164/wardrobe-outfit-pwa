#!/usr/bin/env tsx
import { execSync } from 'child_process';
import * as fs from 'fs';
import * as path from 'path';

/**
 * Prepares test fixtures: generates images, copies to test directories.
 * Run before integration or E2E tests that need fixture data.
 */
function main() {
  console.log('Preparing test fixtures...');
  
  // Generate fixture images
  const fixtureScript = path.join(__dirname, '..', '..', 'tests', 'fixtures', 'generate-images.ts');
  if (fs.existsSync(fixtureScript)) {
    execSync(`npx tsx "${fixtureScript}"`, { stdio: 'inherit' });
  } else {
    console.log('Fixture script not found, installing...');
    fs.mkdirSync(path.join(__dirname, '..', '..', 'tests', 'fixtures', 'images'), { recursive: true });
    const identPng = Buffer.from([
      0x89,0x50,0x4E,0x47,0x0D,0x0A,0x1A,0x0A,
      0x00,0x00,0x00,0x0D,0x49,0x48,0x44,0x52,
      0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01,
      0x08,0x02,0x00,0x00,0x00,0x90,0x77,0x53,
      0xDE,0x00,0x00,0x00,0x0C,0x49,0x44,0x41,
      0x54,0x08,0xD7,0x63,0xF8,0xCF,0xC0,0x00,
      0x00,0x00,0x03,0x00,0x01,0x36,0x28,0x19,
      0x00,0x00,0x00,0x00,0x00,0x49,0x45,0x4E,
      0x44,0xAE,0x42,0x60,0x82,
    ]);
    fs.writeFileSync(path.join(__dirname, '..', '..', 'tests', 'fixtures', 'images', 'fixture-1x1.png'), identPng);
  }
  
  console.log('Test fixtures ready');
}

main();
